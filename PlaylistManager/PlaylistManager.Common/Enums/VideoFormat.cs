namespace PlaylistManager.Common.Enums;

/// <summary>
/// Represents the video media format.
/// </summary>
public enum VideoFormat
{
    /// <summary>
    /// No specific format.
    /// </summary>
    None = 0,

    /// <summary>
    /// MP4 video format.
    /// </summary>
    Mp4 = 1,

    /// <summary>
    /// AVI video format.
    /// </summary>
    Avi = 2,

    /// <summary>
    /// MKV video format.
    /// </summary>
    Mkv = 3,

    /// <summary>
    /// MOV video format.
    /// </summary>
    Mov = 4
}
