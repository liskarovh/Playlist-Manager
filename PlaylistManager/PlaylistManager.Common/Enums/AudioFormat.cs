namespace PlaylistManager.Common.Enums;

/// <summary>
/// Represents the audio media format.
/// </summary>
public enum AudioFormat
{
    /// <summary>
    /// No specific format.
    /// </summary>
    None = 0,

    /// <summary>
    /// MP3 audio format.
    /// </summary>
    Mp3,

    /// <summary>
    /// WAV audio format.
    /// </summary>
    Wav,

    /// <summary>
    /// FLAC audio format.
    /// </summary>
    Flac,

    /// <summary>
    /// AAC audio format.
    /// </summary>
    Aac
}
